public class DietChart {
    String preBreakfast, breakfast, midMorning, lunch, postLunch, dinner;

    public String getPreBreakfast() {
        return preBreakfast;
    }

    public void setPreBreakfast(String preBreakfast) {
        this.preBreakfast = preBreakfast;
    }

    public String getBreakfast() {
        return breakfast;
    }

    public void setBreakfast(String breakfast) {
        this.breakfast = breakfast;
    }

    public String getMidMorning() {
        return midMorning;
    }

    public void setMidMorning(String midMorning) {
        this.midMorning = midMorning;
    }

    public String getLunch() {
        return lunch;
    }

    public void setLunch(String lunch) {
        this.lunch = lunch;
    }

    public String getPostLunch() {
        return postLunch;
    }

    public void setPostLunch(String postLunch) {
        this.postLunch = postLunch;
    }

    public String getDinner() {
        return dinner;
    }

    public void setDinner(String dinner) {
        this.dinner = dinner;
    }
}
